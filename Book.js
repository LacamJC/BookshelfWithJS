class Book{
    constructor(title, pages, author, id)
    {
        this.title = title,
        this.pages = pages, 
        this.author = author,
        this.id = id
    }

    getTitle()
    {
        return this.title;
    };

    getAuthor()
    {
        return this.author;
    };

    getPages()
    {
        return this.pages;
    };

    getAuthor()
    {
        return this.author;
    };
}

module.exports = Book;